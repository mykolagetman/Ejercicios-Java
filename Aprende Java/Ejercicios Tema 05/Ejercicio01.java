public class Ejercicio01 {
	
  public static void main (String[] args) {
    int multiplos;
    
    System.out.println("Los multiplos de 5 de 0 a 100 son: ");
    
    for (multiplos = 0; multiplos <= 100; multiplos = multiplos + 5){
     
      System.out.println(multiplos);
      
    }
  }
}

